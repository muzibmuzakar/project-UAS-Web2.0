<script>
export default{
    data(){
        return {
            products: [],
            product: {
                id: '',
                name: '',
                description: '',
                price: '',
                amount: ''
            },
            add: true,
            edit: false,
            pagination: {},
            carts: [],
            cartadd: {
                id: '',
                name: '',
                price: '',
                amount: ''
            },
            badge: '0',
            quantity: '1',
            totalprice: '0',
            search: '',
            showsearch: false,
            caris: []
        }
    },
    created(){
        this.viewProduct();
        this.viewCart();
    },
    methods: {
        searchProduct(){
            fetch('/api/product/search?q='+this.search)
            .then(res => res.json())
            .then(res => {
                this.caris = res;
                this.search = '';
                this.showsearch = true;
            })
            .catch(err => {
                console.log(err);
            });
        },
        viewCart(){
            if(localStorage.getItem('carts')){
                this.carts = JSON.parse(localStorage.getItem('carts'));
                this.badge = this.carts.length;
                this.totalprice = this.carts.reduce((total, item)=>{
                    return total + item.amount * item.price;
                }, 0);
            }
        },
        addCart(pro){
            this.cartadd.id = pro.id;
            this.cartadd.name = pro.name;
            this.cartadd.price = pro.price;
            this.cartadd.amount = this.quantity;
            this.carts.push(this.cartadd);
            this.cartadd = {};
            this.storeCart();
        },
        removeCart(pro){
            this.carts.splice(pro, 1);
            this.storeCart();
        },
        storeCart(){
            let parsed = JSON.stringify(this.carts);
            localStorage.setItem('carts', parsed);
            this.viewCart();
        },
        viewProduct(pagi){
            pagi = pagi || '/api/products';
            fetch(pagi)
            .then(res => res.json())
            .then(res => {
                this.products = res.data;
                this.pagination = {
                    current_page: res.meta.current_page,
                    last_page: res.meta.last_page,
                    from_page: res.meta.from,
                    to_page: res.meta.to,
                    total_page: res.meta.total,
                    path_page: res.meta.path+"?page=",
                    first_link: res.links.first,
                    last_link: res.links.last,
                    prev_link: res.links.prev,
                    next_link: res.links.next
                };
            })
            .catch(err => console.log(err));
        },
        addProduct(){
            fetch('api/products', {
                method: 'post',
                body: JSON.stringify(this.product),
                headers: {
                    'content-type': 'application/json'
                }
            })
            .then(res => res.json())
            .then(data => {
                swal("Successful!", "Product has been added", "success");
                this.product.name = '';
                this.product.description = '';
                this.product.price = '';
                this.product.amount = '';
                this.viewProduct();
            })
            .catch(err => {
                swal("Failed!", "Product fail to add", "error");
            });
        },
        editProduct(pro){
            this.add = false;
            this.edit = true;
            this.product.id = pro.id
            this.product.name = pro.name
            this.product.description = pro.description
            this.product.price = pro.price
            this.product.amount = pro.amount
        },
        updateProduct(id){
            fetch(`api/products/${id}`, {
                method: 'put',
                body: JSON.stringify(this.product),
                headers: {
                    'content-type': 'application/json'
                }
            })
            .then(res => res.json())
            .then(data => {
                swal("Successful!", "Product has been updated", "success");
                this.add = true;
                this.edit = false;
                this.product.name = '';
                this.product.description = '';
                this.product.price = '';
                this.product.amount = '';
                this.viewProduct();
            })
            .catch(err => {
                swal("Failed!", "Product fail to update", "error");
            });
        },
        deleteProduct(id){
            swal({
                title: "Are you sure?",
                text: "Product will be delete",
                icon: "warning",
                buttons: true,
                dangerMode: true
            }).then((willdelete)=>{
                if(willdelete){
                    
                    fetch(`api/products/${id}`, {
                        method: 'delete'
                    })
                    .then(res => res.json())
                    .then(data => {
                        swal("Successful!", "Product has been deleted", "success");
                        this.viewProduct();
                    })
                    .catch(err => {
                        swal("Failed!", "Product fail to delete", "error");
                    });

                }
            })
        },
        clearProduct(){
            this.add = true;
            this.edit = false;
            this.product.id = '';
            this.product.name = '';
            this.product.description = '';
            this.product.price = '';
            this.product.amount = '';
        },
        viewLink(mylink){
            location.href = '/detail/'+mylink;
        }
    }

}
</script>